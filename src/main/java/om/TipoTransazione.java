package om;

/**
 * TipoTransazione. Enum che permette di distinguere i due tipi di transazioni rappresentabili dalla classe EntityRegistro:
 * in ingresso (prodotto che entra in magazzino) o in uscita (prodotto che viene ceduto).
 *
 * @author Edoardo Baral
 */
public enum TipoTransazione
{
    INGRESSO,
    USCITA
}
